def _get_liu_qin_from_gan(day_gan: str, other_gan: str) -> str:
    """
    根據天干獲取六親（傳統方法）

    Args:
        day_gan: 日天干
        other_gan: 其他天干

    Returns:
        六親名稱
    """
    from bazi.core.constants import TIAN_GAN_WU_XING, TIAN_GAN_YIN_YANG, WU_XING_SHENG, WU_XING_KE

    if not day_gan or not other_gan:
        return ""

    day_wx = TIAN_GAN_WU_XING.get(day_gan, "")
    other_wx = TIAN_GAN_WU_XING.get(other_gan, "")
    day_yy = TIAN_GAN_YIN_YANG.get(day_gan, "")
    other_yy = TIAN_GAN_YIN_YANG.get(other_gan, "")

    if not day_wx or not other_wx:
        return ""

    # 同五行
    if other_wx == day_wx:
        return "比肩" if other_yy == day_yy else "劫財"

    # 生我者（印）
    if WU_XING_SHENG.get(other_wx) == day_wx:
        return "偏印" if other_yy == day_yy else "正印"

    # 我生者（食傷）
    if WU_XING_SHENG.get(day_wx) == other_wx:
        return "食神" if other_yy == day_yy else "傷官"

    # 剋我者（官殺）
    if WU_XING_KE.get(other_wx) == day_wx:
        return "七殺" if other_yy == day_yy else "正官"

    # 我剋者（財）
    if WU_XING_KE.get(day_wx) == other_wx:
        return "偏財" if other_yy == day_yy else "正財"

    return ""


def calculate_ganzhi_xiang(ba_zi: str, shi_shen: dict = None, liu_shi_jia_zi: dict = None) -> dict:
    """
    干支象法計算

    Args:
        ba_zi: 八字字符串（如"甲子 乙丑 丙寅 丁卯"）
        shi_shen: 十神字典（可選）
        liu_shi_jia_zi: 六十甲子體象論數據（可選，用於六親信息）

    Returns:
        {
            "四柱干支象": [
                {
                    "位置": "年柱",
                    "干支": "甲子",
                    "天干臟腑": {"臟腑": "膽", "部位": "頭"},
                    "地支臟腑": {"臟腑": "膀胱"},
                    "干支意象": "直上、坎智",
                    "六親": "父親",
                    "吉凶": "吉",
                },
                ...
            ],
            "五行化氣": list,
            "疾病預測": list,
            "特殊疾病": list,
        }
    """
    pillars = ba_zi.split()
    pillar_names = ["年柱", "月柱", "日柱", "時柱"]

    if len(pillars) != 4:
        return {
            "四柱干支象": [],
            "五行化氣": [],
            "疾病預測": [],
            "特殊疾病": [],
        }

    # 從六十甲子體象論獲取六親分布（按干支查找）
    liu_qin_map = {}
    if liu_shi_jia_zi and "六親類化" in liu_shi_jia_zi:
        for lq in liu_shi_jia_zi["六親類化"]:
            gan_zhi = lq.get("干支", "")
            liu_qin_name = lq.get("六親", "")
            if gan_zhi:
                liu_qin_map[gan_zhi] = liu_qin_name

    # 获取日天干（用於傳統六親計算）
    day_gan = pillars[2][0] if len(pillars) > 2 else ""

    # 四柱干支象 - 每柱獨立顯示
    si_zhu_gan_zhi_xiang = []
    wu_xing_count = {"木": 0, "火": 0, "土": 0, "金": 0, "水": 0}

    for i, pillar in enumerate(pillars):
        gan = pillar[0]
        zhi = pillar[1]
        pillar_name = pillar_names[i]

        # 天干臟腑
        gan_zang_fu = GAN_ZHANG_FU.get(gan, {})
        # 地支臟腑
        zhi_zang_fu = ZHI_ZHANG_FU.get(zhi, {})

        # 干支意象
        gan_yi = GAN_YI_XIANG.get(gan, "")
        zhi_yi = ZHI_YI_XIANG.get(zhi, "")

        # 五行統計
        gan_wx = TIAN_GAN_WU_XING.get(gan, "")
        zhi_wx = ZHI_WU_XING.get(zhi, "")
        if gan_wx:
            wu_xing_count[gan_wx] += 1
        if zhi_wx:
            wu_xing_count[zhi_wx] += 1

        # 六親信息 - 優先使用六十甲子體象論，其次使用傳統天干六親
        liu_qin_name = liu_qin_map.get(pillar, "")
        if not liu_qin_name and day_gan:
            # 使用傳統天干六親
            liu_qin_name = _get_liu_qin_from_gan(day_gan, gan)

        # 吉凶判斷（基於十神）
        ji_xiong = "平"
        if shi_shen and pillar_name in shi_shen:
            # shi_shen 結構：{'年柱': {'柱': '乙亥', '天干': '乙', '天干十神': '七殺', ...}}
            shishen = shi_shen[pillar_name].get("天干十神", "")
            # 吉神：正官、正印、偏印、正財、偏財、食神
            # 凶神：七殺、傷官、比肩、劫財
            ji_shen = ["正官", "正印", "偏印", "正財", "偏財", "食神"]
            xiong_shen = ["七殺", "傷官", "比肩", "劫財"]
            if shishen in ji_shen:
                ji_xiong = "吉"
            elif shishen in xiong_shen:
                ji_xiong = "凶"

        si_zhu_gan_zhi_xiang.append({
            "位置": pillar_name,
            "干支": pillar,
            "天干": gan,
            "地支": zhi,
            "天干臟腑": gan_zang_fu,
            "地支臟腑": zhi_zang_fu,
            "天干意象": gan_yi,
            "地支意象": zhi_yi,
            "六親": liu_qin_name,
            "吉凶": ji_xiong,
        })

    # 五行化氣 - 顯示完整五合
    hua_qi_list = []
    for combo, info in WU_XING_HUA_QI.items():
        hua_qi_list.append(f"{combo}→{info['化']}（{info['說明']}）")

    # 疾病預測
    ji_bing_list = []

    # 檢查過旺
    for wx, count in wu_xing_count.items():
        if count >= 4:
            ji_bing_list.append(f"{wx}過旺（{count}個）：{JI_BING_YU_CE[wx]['過旺']}")

    # 特殊疾病
    te_shu_ji_bing_list = []

    # 土多或過旺
    if wu_xing_count["土"] >= 4:
        te_shu_ji_bing_list.append(TE_SHU_JI_BING["土多或過旺"])

    # 土旺水虛
    if wu_xing_count["土"] >= 3 and wu_xing_count["水"] <= 1:
        te_shu_ji_bing_list.append(TE_SHU_JI_BING["土旺水虛"])

    # 群木剋土
    if wu_xing_count["木"] >= 3 and wu_xing_count["土"] <= 1:
        te_shu_ji_bing_list.append(TE_SHU_JI_BING["群木剋土"])

    return {
        "四柱干支象": si_zhu_gan_zhi_xiang,
        "五行化氣": hua_qi_list,
        "疾病預測": ji_bing_list,
        "特殊疾病": te_shu_ji_bing_list,
    }



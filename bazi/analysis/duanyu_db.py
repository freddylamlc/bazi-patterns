def get_shishen_duan_yu(shishen_name: str, is_ji: bool = False) -> dict:
    """
    獲取十神斷語

    Args:
        shishen_name: 十神名稱
        is_ji: 是否為忌神

    Returns:
        斷語字典
    """
    if shishen_name not in SHISHEN_EVENT_DUAN_YU:
        return {}

    data = SHISHEN_EVENT_DUAN_YU[shishen_name]

    if is_ji:
        return {
            "吉凶": "凶",
            "事件": data.get("凶", []),
            "心性": data.get("心性負面", ""),
        }
    else:
        return {
            "吉凶": "吉",
            "事件": data.get("吉", []),
            "心性": data.get("心性正面", ""),
        }


def get_geju_duan_yu(ge_name: str, is_cheng: bool = True, form: str = None) -> str:
    """
    獲取格局斷語

    Args:
        ge_name: 格局名稱
        is_cheng: 是否成格
        form: 格局形式（如官印相生、財生官等）

    Returns:
        斷語字符串
    """
    if ge_name not in GEJU_CHENGBAI_DUAN_YU:
        return ""

    data = GEJU_CHENGBAI_DUAN_YU[ge_name]

    if is_cheng:
        if form and form in data.get("成格", {}):
            return data["成格"][form]
        return "，".join(data.get("成格", {}).values())
    else:
        return "，".join(data.get("破格", {}).values())


